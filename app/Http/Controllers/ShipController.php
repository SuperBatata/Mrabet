     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        //
        $this->validate($request ,['Part'=>'required_without_all', 'Ancienneté' => 'required_without_all' ,'Position' => 'required_without_all']);
       $ship=new Ship();
        $ship->part=$request->input('Part');
        $ship->position=$request->input('Position');
        $ship->anciennete=$request->input('Ancienneté');
        $ship->save();
        return redirect('/welcome');
    }

